function toDigits(serialized: string): number[] {
  return serialized.split('').map((value) => Number(value));
}

function buildPuzzle(mask: string): number[] {
  const puzzle = new Array<number>(81).fill(0);
  for (let i = 0; i < 81; i += 1) {
    puzzle[i] = mask[i] === '1' ? Number(SOLUTION[i]) : 0;
  }
  return puzzle;
}

export function getPuzzle(difficulty: Difficulty, seed?: number): SudokuPuzzle {
  const bank = masksByDifficulty[difficulty];
  const pickIndex =
    typeof seed === 'number'
      ? Math.floor(createSeededRng(seed)() * bank.length)
      : Math.floor(Math.random() * bank.length);

  return {
    puzzle: buildPuzzle(bank[pickIndex]),
    solution: toDigits(SOLUTION),
    difficulty,
  };
}